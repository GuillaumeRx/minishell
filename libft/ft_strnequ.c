#include "libft.h"

int		ft_strnequ(const char *s1, const char *s2, size_t n)
{
	if (s1 == NULL || s2 == NULL)
		return (0);
	while (n--)
	{
		if (s1[n] != s2[n])
			return (0);
	}
	return (1);
}
